def handle_anamnese(bot, message):
    questions = [
        "🎂 Укажите возраст пациента:",
        "🚻 Укажите пол пациента (м/ж):",
        "♿ Статус по ECOG:",
        "🧬 Диагноз по МКБ-10:",
        "🔬 Гистология:",
        "📍 Стадия TNM:",
        "🧫 Биомаркеры:",
        "🔄 Предыдущее лечение:",
        "🕸️ Метастазы или синхронные опухоли:",
        "🧪 Данные обследования (КТ/МРТ/ПЭТ):"
    ]
    user_id = message.chat.id
    bot.send_message(user_id, "📋 Пожалуйста, ответьте на следующие вопросы по очереди.")
    for q in questions:
        bot.send_message(user_id, q)
